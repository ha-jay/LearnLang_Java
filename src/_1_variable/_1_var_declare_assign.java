package _1_variable;

public class _1_var_declare_assign {
    public static void main(String[] args) {
        //(local) variable declare and assign
        int number; // declare a variable
        number = 1; //assign a value to a variable
        System.out.println(number);
        System.out.println("--------------------------------");
        
        //declare multiple variables
        int b, c, d;
        b = 1;
        c = 2;
        d = 3;
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println("--------------------------------");

        //initialize variables when declare
        int e = 500;
        System.out.println(e);
        System.out.println("--------------------------------");

       
    }
}
